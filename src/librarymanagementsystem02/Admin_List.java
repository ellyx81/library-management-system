    public void findAdmins()
    {
        ArrayList<Admins> admins = ListAdmins(searchbar3.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Admin ID","Username","Password","First Name", "Middle Name", "Last Name", "Phone", "Email", "Gender"});
        Object[] row = new Object[9];
        
        for(int i = 0; i < admins.size(); i++)
        {
            row[0] = admins.get(i).getId();
            row[1] = admins.get(i).getUsername();  
            row[2] = admins.get(i).getPassword();  
            row[3] = admins.get(i).getFname();
            row[4] = admins.get(i).getMname();
            row[5] = admins.get(i).getLname();  
            row[6] = admins.get(i).getPhone();  
            row[7] = admins.get(i).getEmail();
            row[8] = admins.get(i).getGender();
            model.addRow(row);
        }
        
        admintable.setModel(model);
           
    }
     public ArrayList<Admins> ListAdmins(String ValToSearch){
    
        ArrayList<Admins> adminList = new ArrayList<Admins>();
            
        Statement st; 
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `admins` WHERE CONCAT (`adminID`, `username`, `password`, `fname`, `mname`, `lname`, `phone`, `email`, `gender`) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            Admins admins;
            while(rs.next())
            {
                
                admins = new Admins(
                                    rs.getInt("adminID"),
                                    rs.getString("username"),
                                    rs.getString("password"),
                                    rs.getString("fname"),
                                    rs.getString("mname"),
                                    rs.getString("lname"),
                                    rs.getString("phone"),
                                    rs.getString("email"),
                                    rs.getString("gender")
                );
                
                adminList.add(admins);
            
            }
        }
        
        catch(Exception ex){
            System.out.println(ex.getMessage());
        
        }
        return adminList;
    
    }
